SELECT EMPNO,
       ENAME,
       SAL
FROM EMP
WHERE SAL = 10000
   OR SAL = 14000;

-- 문제2
SELECT *
FROM EMP
WHERE ENAME LIKE '%e%' OR ENAME LIKE '%E%';

-- 문제3
SELECT EMPNO,
       ENAME
FROM EMP
WHERE ENAME LIKE 'J%'
   OR ENAME LIKE '%n%';

-- 문제4
SELECT EMPNO,
       DEPTNO,
       ENAME,
       SAL,
       COMM
FROM EMP
WHERE DEPTNO NOT IN (30, 60, 90);

