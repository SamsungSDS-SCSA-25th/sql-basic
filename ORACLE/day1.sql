   OR INSTR(ENAME, 'e') > 0;

-- 문제10
SELECT EMPNO,
       SAL,
       ROUND((COMM / SAL), 4)                AS COMM_RATE,
       ROUND((SAL * (COMM / SAL)) / 1000, 1) AS REAL_SAL
FROM EMP
WHERE COMM IS NOT NULL;

-- 문제11
SELECT EMPNO,
       ENAME,
       SAL,
       CASE
           WHEN SAL >= 20000 THEN 'A'
           WHEN SAL >= 10000 THEN 'B'
           WHEN SAL >= 5000 THEN 'C'
           ELSE 'D'
           END AS SAL_RANK
FROM EMP;

