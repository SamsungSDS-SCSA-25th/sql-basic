SET ECHO ON
    WHENEVER SQLERROR EXIT SQL.SQLCODE

    DEFINE PDB_NAME   = 'FREEPDB1'
    DEFINE SCOTT_USER = 'SCOTT'
    DEFINE SCOTT_PWD  = 'tiger'
    DEFINE SCOTT_TBS  = 'USERS'
    DEFINE SCOTT_TEMP = 'TEMP'

    PROMPT >>> Switching to PDB &&PDB_NAME
ALTER SESSION SET CONTAINER = &&PDB_NAME;

PROMPT >>> Dropping existing user (if exists)
BEGIN
    EXECUTE IMMEDIATE 'DROP USER &&SCOTT_USER CASCADE';
EXCEPTION WHEN OTHERS THEN
    IF SQLCODE != -1918 THEN NULL; ELSE RAISE; END IF;
END;
/

PROMPT >>> Creating user
CREATE USER &&SCOTT_USER IDENTIFIED BY &&SCOTT_PWD
    DEFAULT TABLESPACE &&SCOTT_TBS
    TEMPORARY TABLESPACE &&SCOTT_TEMP
    QUOTA UNLIMITED ON &&SCOTT_TBS;

GRANT CREATE SESSION, CREATE TABLE, CREATE VIEW, CREATE SEQUENCE TO &&SCOTT_USER;

CONNECT &&SCOTT_USER/&&SCOTT_PWD@&&PDB_NAME

PROMPT >>> Creating tables
